    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String asi_login = jLabel7.getText();
        String asi_nombre = txt1.getText();
        String asi_apellido = txt2.getText();
        String asi_email = txt3.getText();
        long asi_celular = Long.parseLong(txt4.getText());
        SimpleDateFormat asi_fecha_nto_obj = new SimpleDateFormat("yyyy-MM-dd");
        String asi_fecha_nto = asi_fecha_nto_obj.format(dtm1.getDate());
        
        attendeeController attendeeUpdate = new attendeeController();
        int update = attendeeUpdate.update(asi_login, asi_nombre, asi_apellido, asi_email, 
            asi_celular, asi_fecha_nto);
        
        if (update == 0){
            JOptionPane.showMessageDialog(null, "El registro fue actualizado exitosamente !");
            dispose();
        }
        else if (update == 1){
            JOptionPane.showMessageDialog(null, "El registro no fue actualizado exitosamente !");            
            dispose();
        }
        else if (update == 2){
            JOptionPane.showMessageDialog(null, "Error al establecer una conexi√≥n de base de datos !");            
            dispose();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

